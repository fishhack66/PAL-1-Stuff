REGSTR	STA $04
	LDA $EF
	STA $00
	LDA $F0
	STA $01
	LDA $F1
	STA $02
	LDA $F2
	STA $03
	STY $05
	STX $06

	JSR $1E2F	; CALL CRLF, PRINT THE OUTPUT HEADER
	LDX #$00
RGSTR1	LDA PCMSG,X
	BEQ PRNTPC	; OUT TO PRINT REGISTERS
	JSR $1EA0
	INX
	JMP RGSTR1


PRNTPC	JSR $1E9E	; CALL OUTSPC
	LDA $00		; PRINT THE HEX VALUE OF PC
	JSR $1E3B	; PRINT A HEX BYTE
	LDA $01
	JSR $1E3B
	LDX #$00
LOOP	LDA $02,X	; LOOP TO GET STORED R VALUES
	JSR $1E9E
	JSR $1E3B
	INX
	CPX #$05
	BNE LOOP

DONE	JSR $1E2F
	JMP $1C00

