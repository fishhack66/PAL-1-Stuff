.ORG = $1000

	CLD
REGSTR	STA $04		; STORE ALL THE REGISTERS
	LDA $EF
	STA $00
	LDA $F0
	STA $01
	LDA $F1
	STA $02
	LDA $F2
	STA $03
	STY $05
	STX $06

	JSR $1E2F	; CALL CRLF, PRINT THE OUTPUT HEADER
	LDX #$00
RGSTR1	LDA PCMSG,X
	BEQ PRNTPC	; OUT TO PRINT REGISTERS
	JSR $1EA0
	INX
	JMP RGSTR1


PRNTPC	JSR $1E9E	; CALL OUTSPC
	LDA $01		; PRINT THE HEX VALUE OF PC (HI/LO)
	JSR $1E3B	; PRINT A HEX BYTE
	LDA $00
	JSR $1E3B
	LDX #$00
LOOP	LDA $02,X	; LOOP TO GET STORED R VALUES
	STA $07	
	JSR $1E9E
	LDA $07
	JSR $1E3B
	INX
	CPX #$05
	BNE LOOP

DONE	JSR $1E2F
	JMP $17FA

